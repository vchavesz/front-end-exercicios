/**
 * Comparações
 */

// Maior que
console.log('Resultado da comparação 5 > 10:');
console.log(5 > 10);

// Menor que
console.log('Resultado da comparação 5 < 10:');
console.log(5 < 10);

// Maior ou igual que
console.log('Resultado da comparação 11 >= 10:');
console.log(11 >= 10);

// Menor ou igual que
console.log('Resultado da comparação 7 <= 7:');
console.log(7 <= 7);

// Igual
console.log('Resultado da comparação 19 == 20:');
console.log(19 == 20);

// Diferente
console.log('Resultado da comparação 19 != 20:');
console.log(19 != 20);

// Igualdade estrita
console.log('Resultado da comparação 19 === \'19\':');
console.log(19 === '19');

// Desigualdade estrita
console.log('Resultado da comparação 20 !== \'20\':');
console.log(20 !== '20');