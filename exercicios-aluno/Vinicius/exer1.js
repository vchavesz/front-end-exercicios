//Faça um script em JavaScript que solicite um número ao usuário e verifique se ele está entre 532 e 743.


let num = prompt('Insira um número entre 532 e 743: ');

console.log(num);

if (num >= 532 && num <= 743) {
    console.log('O número está entre 532 e 743');

} 

else {
    console.log('O número não está entre 532 e 743');
}
